    private void llenarTabla() {
        Jugador jugador = new Jugador();
        String estado = "";
        String Columnas[] = {"idJugador", "Nombre", "Puntaje", "Estado"};
        DefaultTableModel TablaModelo = new DefaultTableModel(Columnas, 0);
        Object[] objetos = new Object[5];
        List lista;
        try {
            lista = controller.findJugadorEntities();
            for (int i = 0; i < lista.size(); i++) {
                jugador = (Jugador) lista.get(i);
                objetos[0] = jugador.getIdJugador();
                objetos[1] = jugador.getNombreJugador();
                objetos[2] = jugador.getAcumuladoPremio();
                objetos[3] = jugador.getEstadoJugador();
                TablaModelo.addRow(objetos);
            }
            this.table1.setModel(TablaModelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos de la tabla.");
        }
    }


