    public void registrar() {
        String nombreJugador = txtNomJu.getText();
        if (!txtNomJu.getText().isEmpty()) {
            try {
                Query RsNombreJu = em.createNativeQuery("SELECT * FROM pregunta WHERE jugadorI =? ORDER BY rand() LIMIT 1", Jugador.class).setParameter(1, jugador.getIdJugador());
                List<Jugador> listaJugadores = RsNombreJu.getResultList();
                if (listaJugadores.isEmpty()) {
                    jugador = new Jugador();
                    jugador.setNombreJugador(nombreJugador);
                    jugador.setIdJugador(0);
                    jugador.setAcumuladoPremio(0);
                    jugador.setEstadoJugador(1);
                    controller.create(jugador);
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario existente, no es posible registrar");
                }
            } catch (Exception e) {
                System.out.println("error registro--->" + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese nombre");

        }
    }

    public void limpiar() {
        txtNomJu.setText("");

    }

